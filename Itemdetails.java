    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
                                            
    try {
        // Establish connection and execute delete query
        Connection con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "scott", "tiger");
        String deleteQuery = "DELETE FROM MENU WHERE Rid = ? AND Iid = ?";
        PreparedStatement deletePS = con.prepareStatement(deleteQuery);
        deletePS.setString(1, restaurantid); // Assuming restaurantid is the Rid
        deletePS.setString(2, itemid); // Assuming itemid is the Iid

        int rowsDeleted = deletePS.executeUpdate();
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Item removed from restaurant menu.");
            // Refresh the table to reflect the changes
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Item removed from restaurant menu.");
        }

        // Close resources
        deletePS.close();
        con.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error deleting item from menu: " + ex.getMessage());
    }

                            
 

                                      

    }//GEN-LAST:event_DeleteActionPerformed

