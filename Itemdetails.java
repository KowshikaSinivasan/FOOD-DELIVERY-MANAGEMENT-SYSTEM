 private void loadData() {
        try {
            String itemQuery = "SELECT * FROM ITEM WHERE Iid = ?";
            ps = con.prepareStatement(itemQuery);
            ps.setString(1, itemid);
            rs = ps.executeQuery();

            if (rs.next()) {
                String itemName = rs.getString("Iname");
                String itemDesc = rs.getString("Idesc");
                double itemPrice = rs.getDouble("price");
                String itemCategory = rs.getString("Category");
                String itemJain = rs.getString("Jain");
                int itemRatings = rs.getInt("Iratings");

                Iid.setText(itemid);
                Itemname.setText(itemName);
                desc.setText(itemDesc);
                price.setText(String.valueOf(itemPrice));
                category.setText(itemCategory);
                jain.setText(itemJain);
                rating.setText(String.valueOf(itemRatings));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
